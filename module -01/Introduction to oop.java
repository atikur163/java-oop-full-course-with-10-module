// EXAMPLE 1: Defining a simple class
// ─────────────────────────────────────────────────────────────
 
class Dog {
    // Fields (attributes)
    String name;
    String breed;
    int age;
 
    // Method (behavior)
    void bark() {
        System.out.println(name + " says: Woof! Woof!");
    }
 
    void displayInfo() {
        System.out.println("Name:  " + name);
        System.out.println("Breed: " + breed);
        System.out.println("Age:   " + age + " years");
    }
}
 
 
